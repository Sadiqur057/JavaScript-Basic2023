let day = 5;

switch(day){
    case 1: 
        console.log("Friday");
        break;
    case 2:
        console.log("Saturday");
        break;
    case 3:
        console.log("Sunday");
        break;
    case 4:
        console.log("Monday");
        break;
    case 5:
        console.log("Tuesday");
        break;
    case 6:
        console.log("Wednesday");
        break;
    case 7:
        console.log("Thursday");
        break;
    default:
        console.log("not a valid day");
}